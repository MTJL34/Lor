#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

function printHelp() {
  console.log(`
Convert MySQL schema SQL to PostgreSQL (Supabase-friendly).

Usage:
  node backEnd/scripts/mysql-to-postgres.js --input <mysql.sql> [--output <postgres.sql>] [--with-data]

Options:
  -i, --input      Input MySQL .sql file (required)
  -o, --output     Output PostgreSQL .sql file (optional)
  --with-data      Keep INSERT statements (default: schema only)
  -h, --help       Show this help
`);
}

function parseArgs(argv) {
  const args = {
    input: null,
    output: null,
    withData: false,
    help: false
  };

  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === '-h' || token === '--help') {
      args.help = true;
      continue;
    }
    if (token === '--with-data') {
      args.withData = true;
      continue;
    }
    if (token === '-i' || token === '--input') {
      args.input = argv[i + 1] || null;
      i += 1;
      continue;
    }
    if (token === '-o' || token === '--output') {
      args.output = argv[i + 1] || null;
      i += 1;
      continue;
    }
  }

  return args;
}

function splitTopLevelComma(input) {
  const parts = [];
  let current = '';
  let depth = 0;
  let inSingle = false;
  let inDouble = false;
  let prev = '';

  for (const ch of input) {
    if (ch === "'" && !inDouble && prev !== '\\') {
      inSingle = !inSingle;
    } else if (ch === '"' && !inSingle && prev !== '\\') {
      inDouble = !inDouble;
    } else if (!inSingle && !inDouble) {
      if (ch === '(') depth += 1;
      if (ch === ')') depth = Math.max(0, depth - 1);
    }

    if (ch === ',' && depth === 0 && !inSingle && !inDouble) {
      parts.push(current.trim());
      current = '';
    } else {
      current += ch;
    }

    prev = ch;
  }

  if (current.trim()) {
    parts.push(current.trim());
  }

  return parts;
}

function normalizeSpaces(value) {
  return value
    .replace(/\s+/g, ' ')
    .trim();
}

function sanitizeIndexColumns(columns) {
  return columns
    .replace(/\s+ASC/gi, '')
    .replace(/\s+DESC/gi, '')
    .replace(/(\w+)\s*\(\s*\d+\s*\)/g, '$1');
}

function quoteIdent(identifier) {
  const raw = identifier.replace(/`/g, '').replace(/"/g, '');
  return `"${raw}"`;
}

function normalizeConstraintExpression(expr) {
  return expr
    .replace(/`/g, '"')
    .replace(/\bREFERENCES\s+([A-Za-z_][A-Za-z0-9_]*)/gi, (_, tableName) => {
      return `REFERENCES ${quoteIdent(tableName)}`;
    });
}

function convertColumnDef(segment) {
  const columnMatch = segment.match(/^("?[A-Za-z_][A-Za-z0-9_]*"?)\s+([\s\S]+)$/);
  if (!columnMatch) {
    return { sql: segment, hasOnUpdate: false };
  }

  const columnName = quoteIdent(columnMatch[1]);
  let def = normalizeSpaces(columnMatch[2]);
  const hasOnUpdate = /\bON UPDATE CURRENT_TIMESTAMP\b/i.test(def);
  const hasAutoIncrement = /\bAUTO_INCREMENT\b/i.test(def);

  def = def
    .replace(/\bAUTO_INCREMENT\b/gi, '')
    .replace(/\bUNSIGNED\b/gi, '')
    .replace(/\bCHARACTER SET\s+[A-Za-z0-9_]+\b/gi, '')
    .replace(/\bCOLLATE\s+[A-Za-z0-9_]+\b/gi, '')
    .replace(/\bON UPDATE CURRENT_TIMESTAMP\b/gi, '')
    .replace(/\bDEFAULT CURRENT_TIMESTAMP\b/gi, 'DEFAULT NOW()')
    .replace(/\bTINYINT\s*\(\s*1\s*\)/gi, 'BOOLEAN')
    .replace(/\bINT\s*\(\s*\d+\s*\)/gi, 'INTEGER')
    .replace(/\bBIGINT\s*\(\s*\d+\s*\)/gi, 'BIGINT')
    .replace(/\bDATETIME\b/gi, 'TIMESTAMPTZ')
    .replace(/\bTIMESTAMP\b/gi, 'TIMESTAMPTZ')
    .replace(/\bJSON\b/gi, 'JSONB')
    .replace(/\bDOUBLE\b/gi, 'DOUBLE PRECISION')
    .replace(/\bMEDIUMTEXT\b/gi, 'TEXT')
    .replace(/\bLONGTEXT\b/gi, 'TEXT')
    .replace(/\bENUM\s*\(([^)]*)\)/gi, 'TEXT');

  if (hasAutoIncrement) {
    if (/\bBIGINT\b/i.test(def)) {
      def = def.replace(/\bBIGINT\b/i, 'BIGINT GENERATED BY DEFAULT AS IDENTITY');
    } else if (/\bINTEGER\b/i.test(def) || /\bINT\b/i.test(def)) {
      def = def.replace(/\b(INTEGER|INT)\b/i, 'INTEGER GENERATED BY DEFAULT AS IDENTITY');
    }
  }

  if (/\bBOOLEAN\b/i.test(def)) {
    def = def
      .replace(/\bDEFAULT\s+0\b/gi, 'DEFAULT FALSE')
      .replace(/\bDEFAULT\s+1\b/gi, 'DEFAULT TRUE');
  }

  def = normalizeSpaces(def);

  return {
    sql: `${columnName} ${def}`,
    hasOnUpdate
  };
}

function convertCreateTable(match, ifNotExists, rawTableName, body) {
  const tableName = quoteIdent(rawTableName);
  const segments = splitTopLevelComma(body);

  const outputSegments = [];
  const postIndexes = [];
  let requiresUpdatedAtTrigger = false;

  for (const rawSegment of segments) {
    const segment = normalizeSpaces(rawSegment.replace(/`/g, '"'));
    if (!segment) continue;

    let keyMatch = segment.match(/^KEY\s+("?[A-Za-z_][A-Za-z0-9_]*"?)\s*\((.+)\)$/i);
    if (keyMatch) {
      const indexName = quoteIdent(keyMatch[1]);
      const columns = sanitizeIndexColumns(keyMatch[2].replace(/`/g, '"'));
      postIndexes.push(`CREATE INDEX IF NOT EXISTS ${indexName} ON ${tableName} (${columns});`);
      continue;
    }

    keyMatch = segment.match(/^INDEX\s+("?[A-Za-z_][A-Za-z0-9_]*"?)\s*\((.+)\)$/i);
    if (keyMatch) {
      const indexName = quoteIdent(keyMatch[1]);
      const columns = sanitizeIndexColumns(keyMatch[2].replace(/`/g, '"'));
      postIndexes.push(`CREATE INDEX IF NOT EXISTS ${indexName} ON ${tableName} (${columns});`);
      continue;
    }

    let uniqueMatch = segment.match(/^UNIQUE\s+KEY\s+("?[A-Za-z_][A-Za-z0-9_]*"?)\s*\((.+)\)$/i);
    if (uniqueMatch) {
      const uniqueName = quoteIdent(uniqueMatch[1]);
      const columns = sanitizeIndexColumns(uniqueMatch[2].replace(/`/g, '"'));
      outputSegments.push(`CONSTRAINT ${uniqueName} UNIQUE (${columns})`);
      continue;
    }

    if (/^PRIMARY KEY\s*\(/i.test(segment)) {
      outputSegments.push(normalizeConstraintExpression(segment));
      continue;
    }

    if (/^CONSTRAINT\s+/i.test(segment) || /^FOREIGN KEY\s*\(/i.test(segment)) {
      outputSegments.push(normalizeConstraintExpression(segment));
      continue;
    }

    const convertedColumn = convertColumnDef(segment);
    if (convertedColumn.hasOnUpdate) {
      requiresUpdatedAtTrigger = true;
    }
    outputSegments.push(convertedColumn.sql);
  }

  const createTable = `CREATE TABLE ${ifNotExists ? 'IF NOT EXISTS ' : ''}${tableName} (\n  ${outputSegments.join(',\n  ')}\n);`;

  return {
    sql: [createTable, ...postIndexes].join('\n'),
    tableName,
    requiresUpdatedAtTrigger
  };
}

function convertMySqlToPostgres(mysqlSql, { withData }) {
  let sql = mysqlSql.replace(/\r\n/g, '\n');
  sql = sql.replace(/`/g, '"');

  sql = sql
    .replace(/^\s*SET\s+NAMES\s+[^;]+;\s*$/gim, '')
    .replace(/^\s*SET\s+FOREIGN_KEY_CHECKS\s*=\s*\d+\s*;\s*$/gim, '')
    .replace(/^\s*START TRANSACTION\s*;\s*$/gim, 'BEGIN;\n')
    .replace(/^\s*\/\*![\s\S]*?\*\/\s*$/gim, '')
    .replace(/^\s*LOCK TABLES[\s\S]*?;\s*$/gim, '')
    .replace(/^\s*UNLOCK TABLES\s*;\s*$/gim, '');

  if (!withData) {
    sql = sql.replace(/^\s*INSERT\s+INTO[\s\S]*?;\s*$/gim, '');
  }

  const triggerTables = [];
  const createTableRegex = /CREATE TABLE\s+(IF NOT EXISTS\s+)?("?[A-Za-z_][A-Za-z0-9_]*"?)\s*\(([\s\S]*?)\)\s*(?:ENGINE\s*=\s*[A-Za-z0-9_]+\s*)?(?:DEFAULT\s+CHARSET\s*=\s*[A-Za-z0-9_]+\s*)?;/gi;

  sql = sql.replace(createTableRegex, (full, ifNotExists, tableName, body) => {
    const converted = convertCreateTable(full, ifNotExists, tableName, body);
    if (converted.requiresUpdatedAtTrigger) {
      triggerTables.push(converted.tableName);
    }
    return converted.sql;
  });

  sql = sql
    .replace(/^\s*--\s*Auto-generated SQL dump[^\n]*\n?/gim, '')
    .replace(/\n{3,}/g, '\n\n')
    .trim();

  const output = [];
  output.push('-- Converted from MySQL to PostgreSQL (Supabase)');
  output.push(`-- Generated at ${new Date().toISOString()}`);
  output.push('-- Review the result before production usage.');
  output.push('');
  output.push(sql);

  if (triggerTables.length) {
    output.push('');
    output.push('-- Keep "updated_at" in sync (replacement for MySQL ON UPDATE CURRENT_TIMESTAMP)');
    output.push('CREATE OR REPLACE FUNCTION public.set_updated_at()');
    output.push('RETURNS TRIGGER AS $$');
    output.push('BEGIN');
    output.push('  NEW.updated_at = NOW();');
    output.push('  RETURN NEW;');
    output.push('END;');
    output.push('$$ LANGUAGE plpgsql;');
    output.push('');

    const uniqueTables = Array.from(new Set(triggerTables));
    for (const tableName of uniqueTables) {
      const triggerName = `"trg_set_updated_at_${tableName.replace(/"/g, '')}"`;
      output.push(`DROP TRIGGER IF EXISTS ${triggerName} ON ${tableName};`);
      output.push(`CREATE TRIGGER ${triggerName}`);
      output.push(`BEFORE UPDATE ON ${tableName}`);
      output.push('FOR EACH ROW');
      output.push('EXECUTE FUNCTION public.set_updated_at();');
      output.push('');
    }
  }

  return `${output.join('\n').trim()}\n`;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    printHelp();
    process.exit(0);
  }

  if (!args.input) {
    console.error('[mysql-to-postgres] Missing required --input <file.sql>');
    printHelp();
    process.exit(1);
  }

  const inputPath = path.resolve(process.cwd(), args.input);
  if (!fs.existsSync(inputPath)) {
    console.error(`[mysql-to-postgres] Input file not found: ${inputPath}`);
    process.exit(1);
  }

  const outputPath = args.output
    ? path.resolve(process.cwd(), args.output)
    : inputPath.replace(/\.sql$/i, '.postgres.sql');

  const source = fs.readFileSync(inputPath, 'utf8');
  const converted = convertMySqlToPostgres(source, { withData: args.withData });

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, converted, 'utf8');

  console.log('[mysql-to-postgres] Conversion complete');
  console.log(`[mysql-to-postgres] Input : ${inputPath}`);
  console.log(`[mysql-to-postgres] Output: ${outputPath}`);
  console.log(`[mysql-to-postgres] Mode  : ${args.withData ? 'schema + data' : 'schema only'}`);
}

main();
